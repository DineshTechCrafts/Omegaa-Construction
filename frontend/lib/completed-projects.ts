function projectPhotos(slug: string, filenames: readonly string[]): string[] {
  return filenames.map((file) => `/images/completed/${slug}/${file}`);
}

function placeholderSlideDescriptions(
  projectName: string,
  photoCount: number,
): string[] {
  const slideCount = Math.ceil(photoCount / 4);
  const phases = [
    'Foundation and structural framing establishing the core layout and load-bearing elements.',
    'Exterior walls, openings, and elevation work shaping the building’s overall form.',
    'Roofing, waterproofing, and exterior finishes protecting and refining the structure.',
    'Interior partitioning, MEP rough-in, and surface preparation across key spaces.',
    'Flooring, tiling, paint, and fitted elements bringing interiors toward completion.',
    'Final detailing, quality checks, and handover-ready finishes across the site.',
    'Additional progress captures documenting workmanship and completed zones.',
    'Project milestone overview highlighting delivered construction quality.',
  ];

  return Array.from({ length: slideCount }, (_, index) => {
    const phase = phases[index] ?? phases[phases.length - 1];
    return `${projectName}: ${phase}`;
  });
}

